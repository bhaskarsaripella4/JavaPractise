public class Calofsi {
    public static void main(String[] args){
        float p,r,si;
        int n;
        p = 1000.50f;
        n = 3;
        r = 15.5f;
        si = p*n*r/100;
        System.out.println(si);
        System.out.println(si+" "+p+" "+r+" "+n);
    }
}
